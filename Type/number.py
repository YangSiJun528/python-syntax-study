# 정수 - Integer
ia = 123
ib = -123
print("정수 :", ia, ib)

# 실수 - Float
fa = 4.24e-3
fb = 4.24e3
fc = 1.2
print("실수 :", fa, fb, fc)

# 8진수와 16진수
oa = 0o177  # o O 상관 없음
xb = 0xAB
print("8, 16 진수 :", oa, xb)

# 연산자
print("1 + 2 :", 1 + 2)  # 더하기
print("3 * 2 :", 3 * 2)  # 곱하기
print("2 ** 4 :", 1 + 2)  # 제곱
print("7 / 2 :", 7 / 2)  # 나누기
print("7 % 2 :", 7 % 2)  # 나머지
print("7 // 2 :", 7 // 2)  # 나누기 몫만
